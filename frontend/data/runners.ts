export function getRunnerBySlug(slug: string): Runner | undefined {
  return runners.find(r => r.slug === slug)
}

export function getRunnersByTeamLevel(teamLevel: 'hs' | 'ms'): Runner[] {
  return runners.filter(r => r.teamLevel === teamLevel)
}

export function getRunnersByGender(gender: 'boys' | 'girls'): Runner[] {
  return runners.filter(r => r.gender === gender)
}

export function getCaptains(): Runner[] {
  return runners.filter(r => r.isCaptain)
}
